#Write a Python program to test whether a passed letter is a vowel or not.

letter=input("Enter any letter : ")     #get value from user
#check condition
if letter=='a' or letter=='e' or letter=='i' or letter=='o' or letter=='u' or letter=='A' or letter=='E' or letter=='I' or letter=='O' or letter=='U':
    print("Letter is vowel.")       #condition are true then print this message.
else:
    print("Letter is not vowel.")   #condition are false then print this message.
